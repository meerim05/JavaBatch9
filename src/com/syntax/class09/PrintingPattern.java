package com.syntax.class09;

public class PrintingPattern {

	public static void main(String[] args) {
		for(int r=1;r<=4;r++) {
			for(int c=1;c<=6;c++) {
			System.out.print("*");
		}
			System.out.println();
		}
		System.out.println("-----BIG SQUARE-------");
	
	for(int r=1;r<=4;r++) {
		for(int c=1; c<=5;c++) {
			System.out.print("$");
		}
		System.out.println();
	}
	System.out.println("--------------SQUARE OF NUMBERS");
		
		
	}
	}


